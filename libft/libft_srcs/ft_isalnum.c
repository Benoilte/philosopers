#include "../includes/libft.h"

/*
Return non-zero value if 'c' is in 0-9 || a-z || A-Z
*/
int	ft_isalnum(int c)
{
	if ((c >= '0' && c <= '9')
		|| (c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z'))
		return (8);
	return (0);
}
